// A realistic autonomous verdict carries a sweep-trace (SKILL.md §sweep-trace mandates it).
const TRACE =
  '\n<!-- sweep-trace: {"ver":1,"issue":1,"gate":"disposition","val":"x","run":"2026-07-01T00:00:00Z","runner":"r","skills":"h"} -->';

describe("isAiAgentComment (marker-based: a machine sweep-trace, not the copyable 🤖 header)", () => {
  it("a real sweep verdict (carries a sweep-trace) is agent — any opener form", () => {
    expect(isAiAgentComment(`> 🤖 AI Agent — issue-sweep\nbody${TRACE}`)).toBe(true);
    expect(isAiAgentComment(`## 🤖 pr-review\n\nverdict${TRACE}`)).toBe(true);
    expect(isAiAgentComment(`@ vm\n\n> 🤖 AI Agent — pr-sweep\nbody${TRACE}`)).toBe(true);
  });

  it("the `_Generated by [Claude Code]` footer also marks a comment agent-authored", () => {
    expect(isAiAgentComment("verdict text\n\n_Generated by [Claude Code]_")).toBe(true);
  });

  it("an HTML-ESCAPED sweep-trace still counts (#1278 double-escape, seen live)", () => {
    expect(isAiAgentComment('verdict\n\n&lt;!-- sweep-trace: {"ver":1} --&gt;')).toBe(true);
  });

  // THE arc#1722 REGRESSION: a HUMAN posting locally via scripts/agent-identity.sh produces
  // the identical `> 🤖 AI Agent @ host · runner:… · skills@…` header. Header-detection read
  // this human Phase-4 directive as an agent verdict and skipped the issue for 19h. With no
  // sweep-trace, it must be HUMAN → the sweep responds.
  it("a HUMAN comment using the 🤖 identity header (no trace) is NOT agent", () => {
    expect(
      isAiAgentComment(
        "> 🤖 AI Agent @ wangshijun-mac-studio · runner:wangshijun · skills@abc-dirty\n\n# Phase 4 plan\n…",
      ),
    ).toBe(false);
  });

  it("the presence-board `> 📡` heartbeat stays non-human even without a trace (#1361)", () => {
    expect(isAiAgentComment("> 📡 presence heartbeat — issue-sweep hourly · no-op")).toBe(true);
  });

  it("plain human comments are not agent", () => {
    expect(isAiAgentComment("LGTM on the proposed fix.")).toBe(false);
    expect(isAiAgentComment("这个我看了,按方案 B 来吧。")).toBe(false);
    expect(isAiAgentComment("I asked the 🤖 AI Agent to fix this")).toBe(false);
    expect(isAiAgentComment("")).toBe(false);
  });
});

describe("labelStance (arc#1722: which of agent:ready / needs-human-confirm was applied last)", () => {
  const ev = (label: string, at: string, action: "labeled" | "unlabeled" = "labeled") => ({
    label,
    action,
    at,
  });
  it("agent:ready applied AFTER needs-human-confirm → work it", () => {
    expect(
      labelStance([
        ev("needs-human-confirm", "2026-07-16T10:00:00Z"),
        ev("agent:ready", "2026-07-17T09:00:00Z"),
      ]),
    ).toBe("work");
  });
  it("needs-human-confirm applied AFTER agent:ready → wait", () => {
    expect(
      labelStance([
        ev("agent:ready", "2026-07-16T10:00:00Z"),
        ev("needs-human-confirm", "2026-07-17T09:00:00Z"),
      ]),
    ).toBe("wait");
  });
  it("only agent:ready → work; only needs-human-confirm → wait; neither → unknown", () => {
    expect(labelStance([ev("agent:ready", "2026-07-16T10:00:00Z")])).toBe("work");
    expect(labelStance([ev("needs-human-confirm", "2026-07-16T10:00:00Z")])).toBe("wait");
    expect(labelStance([ev("P1", "2026-07-16T10:00:00Z")])).toBe("unknown");
  });
  it("a later `unlabeled` clears the label (removed → not counted)", () => {
    expect(
      labelStance([
        ev("agent:ready", "2026-07-16T10:00:00Z"),
        ev("needs-human-confirm", "2026-07-17T09:00:00Z"),
        ev("needs-human-confirm", "2026-07-17T12:00:00Z", "unlabeled"),
      ]),
    ).toBe("work"); // confirm was removed → only ready stands
  });

  // #1831 盲区一:verification-report / e2e-gate 的 HTML 标记必须在正文最前
  // (merge-gate.ts 解析需要),导致 startswith 判据把机器评论误判为人类
  it("recognizes AI comment behind a leading verification-report HTML marker (#1831)", () => {
    expect(
      isAiAgentComment(
        "<!-- verification-report sha=e408e83e1f2a3b4c result=NA -->\n> 🤖 AI Agent Verification @ vm · runner:yechao · skills@e408e83\nreport body",
      ),
    ).toBe(true);
  });

  it("recognizes AI comment behind multiple leading HTML markers (#1831)", () => {
    expect(
      isAiAgentComment(
        '<!-- pr-review-verdict -->\n<!-- sweep-trace: {"ver":1,"pr":1,"gate":"verdict","val":"MERGE","run":"2026-07-17T00:00:00Z"} -->\n> 🤖 AI Agent PR Review @ vm · runner:robert · skills@abc\nverdict',
      ),
    ).toBe(true);
  });

  it("does NOT treat a human comment with a leading HTML marker as AI (#1831)", () => {
    expect(isAiAgentComment("<!-- note-to-self -->\nLGTM, merging this manually.")).toBe(false);
  });

  // #1822 实踩变种 1:MCP double-HTML-encode(#1278)把 marker 变成 &lt;!-- ... --&gt;
  it("recognizes AI comment behind an HTML-entity-escaped marker (#1831/#1278)", () => {
    expect(
      isAiAgentComment(
        "&lt;!-- verification-report sha=e408e83e result=NA --&gt;\n> 🤖 AI Agent Verification @ vm · runner:yechao · skills@e408e83",
      ),
    ).toBe(true);
  });

  // #1822 实踩变种 2:$(cat ...) 未展开的事故评论(#1298 同款),唯一机器信号是
  // Claude Code 自动附加的行首签名
  it("recognizes broken agent comment via Claude Code trailer at line start (#1831/#1298)", () => {
    expect(
      isAiAgentComment(
        "$(cat /tmp/claude-0/scratchpad/pm-final.md)\n\n---\n_Generated by [Claude Code](https://claude.ai/code)_",
      ),
    ).toBe(true);
  });

  it("does NOT treat a human reply quoting an AI comment (with quoted trailer) as AI (#1831)", () => {
    expect(
      isAiAgentComment(
        "please fix the second case too\n\n> 🤖 AI Agent PR Review @ vm\n> some quoted verdict\n> _Generated by [Claude Code](https://claude.ai/code)_",
      ),
    ).toBe(false);
  });

  // arc#1362 archetype: the presence-board heartbeat is script-authored
  // (scripts/presence-heartbeat.ts edits the same comment in place) but carries
  // no sweep-trace and no `> 🤖 AI Agent` marker — without this exception it
  // reads as a fresh human reply and the board gets "re-processed" every round.
  it("recognizes the presence-board `> 📡` heartbeat as non-human (#1361/#1362)", () => {
    expect(
      isAiAgentComment(
        '<!-- presence: {"runner":"Robert Mao"} -->\n> 📡 **runner: Robert Mao** — 由 `scripts/presence-heartbeat.ts` 维护(编辑本 comment,不新发)\n\n| routine | last beat |...',
      ),
    ).toBe(true);
  });

  // arc#1977 archetype: not just the leading HTML comment but the `> 🤖` marker
  // LINE ITSELF arrived double-HTML-encoded (`&gt;` instead of `>`), so the
  // #1278 leading-comment decode alone wasn't enough to recognize it.
  it("recognizes AI comment when the marker line itself is double-HTML-escaped (#1278 variant, arc#1977)", () => {
    expect(
      isAiAgentComment(
        "&lt;!-- test-sweep-report --&gt;\n&gt; 🤖 AI Agent @ vm · runner:yechao · skills@71a1c3c1\n\nreport body",
      ),
    ).toBe(true);
  });
});

