describe("shouldProcess — inline", () => {
  it("zero-comment issue with plain body → keep (unprocessed)", () => {
    const issue: Issue = {
      number: 1,
      comments: [],
      body: "Please fix the flaky login test.",
    };
    expect(shouldProcess(issue)).toBe(true);
  });

  it("zero-comment issue whose body IS an agent comment → skip", () => {
    // This edge case: spin-off body starts with > 🤖 but that's a spin-off author quoting
    // — body of an issue is never actually an agent comment in practice,
    // but we test the predicate logic directly
    const issue: Issue = {
      number: 2,
      comments: [],
      body: "> 🤖 AI Agent — auto-generated spin-off\nDelete sample.test.ts" + TRACE,
    };
    // Body itself IS an AI agent comment → treated as "already processed" → skip
    expect(shouldProcess(issue)).toBe(false);
  });

  it("last comment is human → keep", () => {
    const issue: Issue = {
      number: 3,
      comments: [
        { body: "> 🤖 AI Agent — sweep\nAudit note." + TRACE },
        { body: "I've confirmed the bug in staging. LGTM." },
      ],
    };
    expect(shouldProcess(issue)).toBe(true);
  });

  it("last comment is terminal AI → skip", () => {
    const issue: Issue = {
      number: 4,
      comments: [
        { body: "Confirmed." },
        { body: "> 🤖 AI Agent — sweep\nFix delivered in PR #99. Closing." + TRACE },
      ],
    };
    expect(shouldProcess(issue)).toBe(false);
  });

  it("last comment is non-terminal AI (deferred) → keep and re-process", () => {
    const issue: Issue = {
      number: 5,
      comments: [
        { body: "Good idea." },
        { body: "> 🤖 AI Agent — sweep 2026-05-20\n🟠 不在本轮范围 / 留开放。" + TRACE },
      ],
    };
    expect(shouldProcess(issue)).toBe(true);
  });

  it("single AI-audit-only comment → skip (terminal audit, no human reply)", () => {
    const issue: Issue = {
      number: 6,
      comments: [
        { body: "> 🤖 AI Agent — doc-audit\nDocument is drifted. needs-human-confirm." + TRACE },
      ],
    };
    expect(shouldProcess(issue)).toBe(false);
  });

  // #2271 archetype: test-sweep opens per-blocklet failure issues directly (not through
  // issue-publish's test-sweep-key marker path), so the body carries the `> 🤖 AI Agent`
  // identity header but no MACHINE_MARKER. Before the label-aware fix this was a false
  // positive: shouldProcess() read a zero-comment automated QA finding as an unprocessed
  // human directive. See SKILL.md Step 3b "test-sweep 发现的 issue".
  it("zero-comment test-sweep-failure issue → skip (automated QA finding, not human input)", () => {
    const issue: Issue = {
      number: 2271,
      labels: ["P2", "test-sweep-failure"],
      comments: [],
      body: "test-sweep 失败报告...\n\n> 🤖 AI Agent @ vm · runner:yechao · skills@56f6c81c",
    };
    expect(shouldProcess(issue)).toBe(false);
  });

  it("zero-comment test-sweep-report (parent QA report) → skip, same rule", () => {
    const issue: Issue = {
      number: 2274,
      labels: ["test-sweep-report"],
      comments: [],
      body: "[test-sweep] QA Report — 2026-07-22 20:08 UTC\n\n> 🤖 AI Agent @ vm · runner:yechao · skills@56f6c81c",
    };
    expect(shouldProcess(issue)).toBe(false);
  });

  it("a test-sweep-failure issue that LATER gets a real human reply is still kept normally", () => {
    // Once a human actually comments, the ordinary last-comment rule takes back over —
    // the label-based override only applies to the zero-comment gap.
    const issue: Issue = {
      number: 2271,
      labels: ["P2", "test-sweep-failure"],
      comments: [{ body: "这个登录墙是有意的,不是回归,可以关闭。" }],
    };
    expect(shouldProcess(issue)).toBe(true);
  });
});

